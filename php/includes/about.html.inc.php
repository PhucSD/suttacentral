<h1>About SuttaCentral</h1>

<p><strong>SuttaCentral</strong> aims at facilitating the study of Buddhist texts from comparative and historical perspectives. It focuses on the texts that represent “Early Buddhism”, texts preserved not only in the Pali Sutta and Vinaya Piṭakas but also in Chinese and Tibetan translations and in fragmentary remains in Sanskrit and other languages.</p>

<p><strong>SuttaCentral</strong> offers a gateway to this material by enabling users to quickly identify the Chinese, Tibetan, and/or Sanskrit parallels of any given Pali discourses, or vice versa. Having found that information, one can, by clicking on the relevant links, consult the actual texts or view available translations.</p>

<p>The system focuses initially on providing the correspondence data from the perspective of the Pali suttas; that is, given a particular Pali sutta, one can find the parallels in other textual languages. Finding parallels in the reverse direction is already possible, but the coverage is incomplete, pending completion of the relevant data-entry work.</p>

<p>In building <strong>SuttaCentral</strong>, we plan to work through the nikāyas, one by one, in the traditional sequence. At present, work on the Dīgha and Majjhima Nikāyas is essentially completed. For the Saṃyutta and Aṅguttara Nikāyas, however, much research remains to be done and the adequacy and reliability of the information provided here cannot be guaranteed.</p>

<p>This collection of data offers substantial improvements over that provided in Akanuma’s pioneering work, <i>Comparative Catalogue of Chinese Āgamas &amp; Pāli Nikāyas</i>, 1929, until now the standard reference work in this area. Nevertheless, there is much room for improvement. We therefore invite other scholars working on this same early Buddhist material to provide input to SuttaCentral (see <a href="http://www.SuttaCentral.net/contacts.htm">Contacts</a>), so that the material displayed can be continually refined for greater accuracy and completeness.</p>

<p>We hope, by providing this service, to advance the study of Early Buddhism. We also hope to promote recognition of the need for such study to take account not only of the Pali texts but also of their counterparts in other languages.</p>
